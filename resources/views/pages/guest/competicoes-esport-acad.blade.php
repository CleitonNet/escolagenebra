        <h2>Líderes de Torcida</h2>
        <p>Você já deve ter visto cheerleaders em filmes norte-americanos – as meninas com roupas de ginástica e pom-poms se apresentando antes de partidas esportivas, com coreografias acrobáticas e gritos de guerra. Cheerleading, ou liderar uma torcida, ao pé da letra, basicamente é uma performance de habilidades atléticas, com dança, saltos e canto para ater a atenção de uma plateia em um evento. Nossas meninas estão sempre presentes em nossas competições e abertura de eventos!</p>

